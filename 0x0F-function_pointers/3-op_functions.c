#include "3-calc.h"

/**
 * op_add - function that performs addition
 * @a: int parameter to be passed
 * @b: int parameter to be passed
 * Return: sum of the two int values
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that performs subtracts
 * @a: int parameter to be passed
 * @b: int parameter to be passed
 * Return: difference of the two int values
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that performs multiplication
 * @a: int parameter to be passed
 * @b: int parameter to be passed
 * Return: multiplication of the two int values
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that performs division
 * @a: int parameter to be passed
 * @b: int parameter to be passed
 * Return: divided of the two int values
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that performs modulus
 * @a: int parameter to be passed
 * @b: int parameter to be passed
 * Return: remainder of the two int values
 */
int op_mod(int a, int b)
{
	return (a % b);
}
